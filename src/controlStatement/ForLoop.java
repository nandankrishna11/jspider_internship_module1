package controlStatement;

public class ForLoop {
    public static void main(String[] args) {
        for (int i=1;i<=5;i++){
            System.out.println(i+": java");
        }
        System.out.println(" ");
        for (int x=5;x>=1;x--){
            System.out.println(x+": Java");
        }
        System.out.println(" ");

        for(int p=1;p<=20;p+=2)
        {
            System.out.println(p+ ":Developer");
        }



    }


}
